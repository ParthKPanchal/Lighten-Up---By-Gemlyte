<script>
  const filterButtons = document.querySelectorAll(".filter-btn");
  const productCards = document.querySelectorAll(".product-card-item");

  // Click handler for category buttons
  filterButtons.forEach((a) => {
    a.addEventListener("click", () => {
      const filter = a.dataset.filter;

      // Highlight the active button
      filterButtons.forEach((btn) => btn.classList.remove("active"));
      a.classList.add("active");

      // Show only matching products
      productCards.forEach((card) => {
        card.style.display = (card.dataset.category === filter) ? "block" : "none";
      });
    });
  });

  // âœ… Auto-select "Fan" tab on page load
  window.addEventListener("DOMContentLoaded", () => {
    document.querySelector('[data-filter="fan"]').click();
  });
</script>
